package com.example.messagingstompwebsocket;

/**
 * A struct to hold two numbers, for the parallel compute task.
 *
 */
public class TwoNumbers {
	private Long n1, n2;

	public Long getN1() {
		return n1;
	}

	public void setN1(Long n1) {
		this.n1 = n1;
	}

	public Long getN2() {
		return n2;
	}

	public void setN2(Long n2) {
		this.n2 = n2;
	}
	
	
	
}
